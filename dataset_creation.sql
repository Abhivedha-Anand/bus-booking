-- 1. USERS TABLE
create database bus_booking;
use bus_booking;

CREATE TABLE users (
    user_id INT AUTO_INCREMENT PRIMARY KEY,
    user_name VARCHAR(100) NOT NULL,
    user_number VARCHAR(15) NOT NULL UNIQUE,
    user_password VARCHAR(255) NOT NULL, 
    user_email VARCHAR(100) UNIQUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- 2. PLACES TABLE (Normalization for reuse and consistency)
CREATE TABLE places (
    place_id INT AUTO_INCREMENT PRIMARY KEY,
    place_name VARCHAR(100) NOT NULL UNIQUE
);

-- 3. BUS TABLE
CREATE TABLE bus (
    bus_id INT AUTO_INCREMENT PRIMARY KEY,
    bus_name VARCHAR(100) NOT NULL,
    from_place_id INT,
    to_place_id INT,
    departure_time TIME,
    arrival_time TIME,
    seat_capacity INT CHECK (seat_capacity >= 0),
    available_seats INT CHECK (available_seats >= 0),
    travel_date DATE,
    FOREIGN KEY (from_place_id) REFERENCES places(place_id),
    FOREIGN KEY (to_place_id) REFERENCES places(place_id)
);

-- 4. BUS FARE TABLE
CREATE TABLE bus_fare (
    fare_id INT AUTO_INCREMENT PRIMARY KEY,
    bus_id INT,
    distance_km INT CHECK (distance_km >= 0),
    fare_amount DECIMAL(8,2) CHECK (fare_amount >= 0),
    FOREIGN KEY (bus_id) REFERENCES bus(bus_id) ON DELETE CASCADE
);

-- 5. TICKETS TABLE
CREATE TABLE tickets (
    ticket_id INT AUTO_INCREMENT PRIMARY KEY,
    bus_id INT NOT NULL,
    user_id INT NOT NULL,
    travel_date DATE NOT NULL,
    adult_count INT DEFAULT 0 CHECK (adult_count >= 0),
    child_count INT DEFAULT 0 CHECK (child_count >= 0),
    infant_count INT DEFAULT 0 CHECK (infant_count >= 0),
    total_fare DECIMAL(10,2) NOT NULL CHECK (total_fare >= 0),
    booking_status ENUM('confirmed', 'cancelled', 'pending') DEFAULT 'pending',
    booked_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    FOREIGN KEY (bus_id) REFERENCES bus(bus_id),
    FOREIGN KEY (user_id) REFERENCES users(user_id)
);

